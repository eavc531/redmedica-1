     */
    public function index()
    {
        $cities = city::all();

        foreach ($cities as $city) {
          $coordinates = Geocoder::getCoordinatesForAddress($city->name,$city->state,'mexico');
          $city->longitud = $coordinates['lng'];
          $city->latitud = $coordinates['lat'];
          $city->save();
        }

    }

